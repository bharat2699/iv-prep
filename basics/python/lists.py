# List = Array which is Heterogeneous

my_list = ["bharat", "SDE2", 24]
my_list_2 = ["name", "designation", "age"]

# Slicing
print(my_list[:2])  # From beginning to 2nd index(excluded)
# op: ['bharat', 'SDE2']

print(my_list[-2])  # Access element at -2 i.e == (len(my_list) -2)
# op: SDE2

print(my_list[:-2])  # From beginning to (len(my_list) -2)th index(excluded)
# op: ['bharat']


